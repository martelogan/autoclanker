@covers("M8-001")
def test_bigbets_rejects_unknown_explicit_schema_version() -> None:
    payload: dict[str, object] = {
        "schema_version": "bigbets.registry.v999",
        "big_bets": [],
        "idea_families": [],
    }

    with pytest.raises(ValidationFailure, match="unsupported schema_version"):
        validate_bigbets_registry(payload)


@covers("M8-001")
def test_bigbets_rejects_ambiguous_wave_priority_and_roles() -> None:
    payload = registry_to_input_payload(load_bigbets_registry(EXAMPLE))
    assert isinstance(payload["big_bets"], list)
    assert isinstance(payload["idea_families"], list)

    bad_priority = json.loads(json.dumps(payload))
    bad_priority["big_bets"][0]["priority"] = "P2"
    with pytest.raises(ValidationFailure, match="wave 1 requires 'P0'"):
        validate_bigbets_registry(bad_priority)

    bad_role = json.loads(json.dumps(payload))
    bad_role["idea_families"][0]["role"] = "custom-label"
    with pytest.raises(ValidationFailure, match="unsupported role"):
        validate_bigbets_registry(bad_role)
